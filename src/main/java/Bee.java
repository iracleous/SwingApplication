public class Bee {

    private int x;
    private int y;

    private String message;

    public void setX( int newX){
        x = newX;
    }
    public void setY( int newY){
        y = newY;
    }
    public int getX(){
        return x;
    }
    public int getY(){
        return y;
    }


    public void setMessage(String mess){
         message = mess ;
    }

    public String getMessage(){
        return message;
    }

}
